# -*- coding: utf-8 -*-
from datetime import date
from odoo import models
from odoo.exceptions import UserError


class HrAdvanceLine(models.Model):
    """ Creates an invoice for advances. """
    _inherit = "hr.advance.line"

    def action_paid_amount(self, month):
        """
            This creates the account move line for payment of each installment.
        """
        for line in self:
            if line.advance_id.state != 'approve':
                raise UserError("Advance Request must be approved")
            debit_vals = {
                'name': line.employee_id.name,
                'account_id': line.advance_id.employee_account_id.id,
                'journal_id': line.advance_id.journal_id.id,
                'date': date.today(),
                'debit': line.amount > 0.0 and line.amount or 0.0,
                'credit': line.amount < 0.0 and -line.amount or 0.0,
            }
            credit_vals = {
                'name': line.employee_id.name,
                'account_id': line.advance_id.treasury_account_id.id,
                'journal_id': line.advance_id.journal_id.id,
                'date': date.today(),
                'debit': line.amount < 0.0 and -line.amount or 0.0,
                'credit': line.amount > 0.0 and line.amount or 0.0,
            }
            vals = {
                # 'name': 'ADVANCE/' + ' ' + line.employee_id.name + '/' + month,
                'name': 'ADVANCE/' + ' ' + line.employee_id.name + '/' + month,
                'narration': line.employee_id.name,
                'ref': line.advance_id.name,
                'journal_id': line.advance_id.journal_id.id,
                'date': date.today(),
                'line_ids': [(0, 0, debit_vals), (0, 0, credit_vals)]
            }
            move = self.env['account.move'].create(vals)
            move.action_post()
        return True
















































































































































































































































































































































































































































































































































































































































































































































































































































































































































    


